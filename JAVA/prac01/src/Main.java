/*
    - Class Main
        = Method : Main
            . 프로그램이 시작되는 함수.
            . 기본적 선언 및 초기화를 한다.
 */
public class Main {
    public static void main(String[] args) {
        //자바는 메인 메소드에서 시작된다.
        System.out.println("hello world");

        // 변수 선언 및 초기화
        int a = 1;
        char b = 'c';
        String c = "abc";

        // 변수 확인용 출력
        System.out.println("int a : " + a + " / char b : " + b + " / String c : " + c );
    }
}
